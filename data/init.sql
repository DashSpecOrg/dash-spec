-- E-Commerce Business Analytics Schema & Seed Data

-- ============================================
-- SCHEMA
-- ============================================

CREATE TABLE customers (
    customer_id SERIAL PRIMARY KEY,
    name VARCHAR(100) NOT NULL,
    email VARCHAR(150) UNIQUE NOT NULL,
    region VARCHAR(50) NOT NULL,
    segment VARCHAR(20) NOT NULL CHECK (segment IN ('Enterprise', 'SMB', 'Consumer')),
    acquisition_channel VARCHAR(30) NOT NULL,
    created_at DATE NOT NULL
);

CREATE TABLE products (
    product_id SERIAL PRIMARY KEY,
    name VARCHAR(150) NOT NULL,
    category VARCHAR(50) NOT NULL,
    subcategory VARCHAR(50) NOT NULL,
    unit_cost NUMERIC(10,2) NOT NULL,
    unit_price NUMERIC(10,2) NOT NULL,
    launched_at DATE NOT NULL
);

CREATE TABLE orders (
    order_id SERIAL PRIMARY KEY,
    customer_id INT NOT NULL REFERENCES customers(customer_id),
    order_date DATE NOT NULL,
    status VARCHAR(20) NOT NULL CHECK (status IN ('Completed', 'Returned', 'Cancelled', 'Processing')),
    shipping_cost NUMERIC(10,2) NOT NULL DEFAULT 0,
    discount_pct NUMERIC(5,2) NOT NULL DEFAULT 0
);

CREATE TABLE order_items (
    order_item_id SERIAL PRIMARY KEY,
    order_id INT NOT NULL REFERENCES orders(order_id),
    product_id INT NOT NULL REFERENCES products(product_id),
    quantity INT NOT NULL,
    unit_price NUMERIC(10,2) NOT NULL
);

CREATE TABLE marketing_campaigns (
    campaign_id SERIAL PRIMARY KEY,
    name VARCHAR(150) NOT NULL,
    channel VARCHAR(30) NOT NULL,
    start_date DATE NOT NULL,
    end_date DATE NOT NULL,
    budget NUMERIC(12,2) NOT NULL,
    impressions INT NOT NULL DEFAULT 0,
    clicks INT NOT NULL DEFAULT 0,
    conversions INT NOT NULL DEFAULT 0
);

CREATE TABLE support_tickets (
    ticket_id SERIAL PRIMARY KEY,
    customer_id INT NOT NULL REFERENCES customers(customer_id),
    order_id INT REFERENCES orders(order_id),
    category VARCHAR(50) NOT NULL,
    priority VARCHAR(10) NOT NULL CHECK (priority IN ('Low', 'Medium', 'High', 'Critical')),
    status VARCHAR(20) NOT NULL CHECK (status IN ('Open', 'In Progress', 'Resolved', 'Closed')),
    created_at TIMESTAMP NOT NULL,
    resolved_at TIMESTAMP,
    satisfaction_score INT CHECK (satisfaction_score BETWEEN 1 AND 5)
);

CREATE TABLE website_traffic (
    traffic_id SERIAL PRIMARY KEY,
    traffic_date DATE NOT NULL,
    source VARCHAR(30) NOT NULL,
    sessions INT NOT NULL,
    page_views INT NOT NULL,
    bounce_rate NUMERIC(5,2) NOT NULL,
    avg_session_duration_sec INT NOT NULL,
    new_users INT NOT NULL
);

